package homework_week_5;

public class Program_10_ProductOfTwoNumbers {
    /**
     * 10. Write a Java program that takes two numbers as input and display the
     * product of two numbers.
     * Test Data:
     * Input first number: 25
     * Input second number: 5
     * Expected Output : 25 x 5 = 125
     */

        public static void main(String[] args) {
            int firstNumber = 25;
            int secondNumber = 5;

            int product= firstNumber * secondNumber;
            //Expected output
            System.out.println(firstNumber + " x " + secondNumber + " = " + product);
        }
    }


