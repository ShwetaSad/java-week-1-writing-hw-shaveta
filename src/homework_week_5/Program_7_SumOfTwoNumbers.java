package homework_week_5;

public class Program_7_SumOfTwoNumbers {
    /**
     * 7. Write a Java program to print the sum of two numbers.
     * Test Data: 74 + 36
     * Expected Output: 110
     */
    public static void main(String[] args) {
        int value1 = 74;
        int value2 = 36;
        int sum = value1 + value2;
        System.out.println("The sum of " + value1 + " and " + value2 + " is " + sum);
    }
}
